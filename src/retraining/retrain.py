"""
retrain.py - Phase 6: Automated Retraining

Reads the drift report from Phase 5 and decides whether to retrain.
If drift is significant, it:
    1. Re-runs feature engineering on current data
    2. Trains a new challenger model (Random Forest — same type as champion)
    3. Compares challenger AUC-ROC against the current champion
    4. Promotes the challenger only if it outperforms the champion

ALL outcomes are logged to MLflow:
    - no_drift_skipped  → drift check passed, no retraining needed
    - retrain_rejected  → challenger trained but lost to champion
    - retrain_challenger → challenger won, promoted to production

Usage:
    python -m src.retraining.retrain
"""

import json
import mlflow
import mlflow.sklearn
import pandas as pd
import numpy as np
from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import train_test_split
from sklearn.metrics import roc_auc_score, precision_score, recall_score, f1_score
from src.utils.config import Config
from src.utils.db import engine
from src.feature_engineering.build_features import run_feature_engineering
from src.drift_detection.detect_drift import run_drift_detection


# ---------------------------------------------------------------------------
# Drift report reader
# ---------------------------------------------------------------------------

def load_drift_report():
    """Read the drift report generated by Phase 5."""
    report_path = Config.INTERIM_DATA_DIR / "drift_report.json"
    if not report_path.exists():
        return None
    with open(report_path, "r") as f:
        return json.load(f)


# ---------------------------------------------------------------------------
# Champion model loader
# ---------------------------------------------------------------------------

def load_champion_metrics():
    """
    Retrieve the current champion model's AUC from MLflow.

    Scans the last 20 runs and finds the most recent non-retrain run
    that has an auc_roc metric. This avoids fragile filter strings
    that can fail across MLflow versions.
    """
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)
    client = mlflow.tracking.MlflowClient()

    experiment = client.get_experiment_by_name("churn_prediction")
    if experiment is None:
        print("  [WARNING] No 'churn_prediction' experiment found in MLflow.")
        return None

    # Get last 20 runs — no filter, we inspect manually
    runs = client.search_runs(
        experiment_ids=[experiment.experiment_id],
        order_by=["start_time DESC"],
        max_results=20,
    )

    if not runs:
        print("  [WARNING] No runs found in 'churn_prediction' experiment.")
        return None

    # Find most recent run that has auc_roc logged and is not a retrain run
    for run in runs:
        run_name = run.data.tags.get("mlflow.runName", "")
        auc = run.data.metrics.get("auc_roc", None)

        # Skip retrain runs — they are challengers, not champions
        if run_name.startswith("retrain") or run_name.startswith("no_drift"):
            continue

        if auc is not None and auc > 0:
            print(f"  Champion found: run='{run_name}', AUC={auc}")
            return {
                "run_id":    run.info.run_id,
                "run_name":  run_name,
                "auc_roc":   auc,
                "f1_score":  run.data.metrics.get("f1_score", 0),
                "precision": run.data.metrics.get("precision", 0),
                "recall":    run.data.metrics.get("recall", 0),
            }

    print("  [WARNING] No valid champion run found. Using AUC=0 as baseline.")
    return {"run_id": None, "run_name": "none", "auc_roc": 0.0,
            "f1_score": 0.0, "precision": 0.0, "recall": 0.0}


def load_champion_feature_names():
    """
    Load the registered champion model and return its feature list.
    Training the challenger on the same features ensures a fair comparison.
    """
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)

    for registry_name in ["churn_predictor", "churn_xgboost"]:
        try:
            champion = mlflow.sklearn.load_model(f"models:/{registry_name}/latest")
            if hasattr(champion, "feature_names_in_"):
                names = list(champion.feature_names_in_)
                print(f"  Champion features loaded from '{registry_name}': {len(names)} features")
                return names
        except Exception:
            continue

    print("  [WARNING] Could not load champion feature names. Using all features.")
    return None


# ---------------------------------------------------------------------------
# Challenger model training
# ---------------------------------------------------------------------------

def train_challenger(champion_feature_names=None):
    """
    Train a Random Forest challenger using params from params.yaml.
    Uses the same feature set as the champion for a fair comparison.
    """
    params = Config.load_params()
    model_params = params["model"]
    training_params = params["training"]

    with engine.connect() as conn:
        df = pd.read_sql("SELECT * FROM churn_features", conn)

    X = df.drop(columns=["account_id", "churn_flag"])
    y = df["churn_flag"]

    # Match champion's feature set
    if champion_feature_names is not None:
        available = [f for f in champion_feature_names if f in X.columns]
        missing = [f for f in champion_feature_names if f not in X.columns]
        if missing:
            print(f"  [WARNING] {len(missing)} champion features missing from data: {missing[:5]}")
        X = X[available]

    X_train, X_test, y_train, y_test = train_test_split(
        X, y,
        test_size=training_params["test_size"],
        random_state=model_params["random_state"],
        stratify=y,
    )

    print(f"  Training challenger: {len(X_train)} train rows, {len(X_test)} test rows, {X_train.shape[1]} features")

    model = RandomForestClassifier(
        n_estimators=model_params.get("n_estimators", 300),
        max_depth=model_params.get("max_depth", 5),
        min_samples_leaf=model_params.get("min_samples_leaf", 5),
        class_weight=model_params.get("class_weight", "balanced"),
        random_state=model_params["random_state"],
    )
    model.fit(X_train, y_train)

    y_pred_proba = model.predict_proba(X_test)[:, 1]
    threshold = model_params.get("threshold", 0.5)
    y_pred = (y_pred_proba >= threshold).astype(int)

    metrics = {
        "auc_roc":   round(roc_auc_score(y_test, y_pred_proba), 4),
        "precision": round(precision_score(y_test, y_pred, zero_division=0), 4),
        "recall":    round(recall_score(y_test, y_pred, zero_division=0), 4),
        "f1_score":  round(f1_score(y_test, y_pred, zero_division=0), 4),
    }

    print(f"  Challenger metrics: {metrics}")
    return model, metrics, X_test.columns.tolist()


# ---------------------------------------------------------------------------
# Champion vs Challenger comparison
# ---------------------------------------------------------------------------

def compare_models(champion_metrics, challenger_metrics):
    """Challenger must strictly exceed champion AUC to be promoted."""
    champion_auc   = champion_metrics.get("auc_roc", 0)
    challenger_auc = challenger_metrics.get("auc_roc", 0)

    return {
        "champion_auc":       champion_auc,
        "challenger_auc":     challenger_auc,
        "improvement":        round(challenger_auc - champion_auc, 4),
        "promote_challenger": challenger_auc > champion_auc,
    }


# ---------------------------------------------------------------------------
# MLflow logging — always runs regardless of outcome
# ---------------------------------------------------------------------------

def log_outcome(run_name, metrics, comparison, feature_columns, verdict, skipped=False):
    """
    Log the retraining outcome to MLflow.

    Always called — even for skipped runs — so there is always a visible
    audit trail in the MLflow UI regardless of what happened.
    """
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)
    mlflow.set_experiment("churn_prediction")

    params = Config.load_params()["model"]

    with mlflow.start_run(run_name=run_name):
        # Run context
        mlflow.log_param("trigger",      "drift_retraining")
        mlflow.log_param("drift_verdict", verdict)
        mlflow.log_param("skipped",       skipped)

        if not skipped:
            # Model params
            mlflow.log_param("model_type",       "random_forest")
            mlflow.log_param("n_estimators",     params.get("n_estimators", 300))
            mlflow.log_param("max_depth",        params.get("max_depth", 5))
            mlflow.log_param("min_samples_leaf", params.get("min_samples_leaf", 5))
            mlflow.log_param("class_weight",     params.get("class_weight", "balanced"))
            mlflow.log_param("num_features",     len(feature_columns))
            mlflow.log_param("promoted",         comparison["promote_challenger"])

            # Metrics
            for name, value in metrics.items():
                mlflow.log_metric(name, value)
            mlflow.log_metric("champion_auc", comparison["champion_auc"])
            mlflow.log_metric("improvement",  comparison["improvement"])

            # Register as new production version only if promoted
            if comparison["promote_challenger"]:
                # model is passed via closure from run_retraining
                pass  # handled in run_retraining after this call

        run_id = mlflow.active_run().info.run_id

    return run_id


def log_and_promote(model, metrics, comparison, feature_columns, verdict):
    """Log challenger run and register model if promoted."""
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)
    mlflow.set_experiment("churn_prediction")

    run_name = (
        "retrain_challenger" if comparison["promote_challenger"]
        else "retrain_rejected"
    )

    params = Config.load_params()["model"]

    with mlflow.start_run(run_name=run_name):
        mlflow.log_param("model_type",       "random_forest")
        mlflow.log_param("n_estimators",     params.get("n_estimators", 300))
        mlflow.log_param("max_depth",        params.get("max_depth", 5))
        mlflow.log_param("min_samples_leaf", params.get("min_samples_leaf", 5))
        mlflow.log_param("class_weight",     params.get("class_weight", "balanced"))
        mlflow.log_param("trigger",          "drift_retraining")
        mlflow.log_param("drift_verdict",    verdict)
        mlflow.log_param("num_features",     len(feature_columns))
        mlflow.log_param("promoted",         comparison["promote_challenger"])

        for name, value in metrics.items():
            mlflow.log_metric(name, value)
        mlflow.log_metric("champion_auc", comparison["champion_auc"])
        mlflow.log_metric("improvement",  comparison["improvement"])

        if comparison["promote_challenger"]:
            mlflow.sklearn.log_model(
                model,
                name="model",
                registered_model_name="churn_predictor",
            )
            print("  Challenger PROMOTED → registered as new churn_predictor version.")
        else:
            mlflow.sklearn.log_model(model, name="model")
            print("  Challenger REJECTED → champion remains in production.")

        return mlflow.active_run().info.run_id


def log_skipped(verdict):
    """Log a lightweight 'skipped' run so the audit trail is always complete."""
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)
    mlflow.set_experiment("churn_prediction")

    with mlflow.start_run(run_name="no_drift_skipped"):
        mlflow.log_param("trigger",       "drift_retraining")
        mlflow.log_param("drift_verdict", verdict)
        mlflow.log_param("skipped",       True)
        mlflow.log_param("reason",        "No significant drift — champion remains in production.")
        return mlflow.active_run().info.run_id


# ---------------------------------------------------------------------------
# Pipeline orchestration
# ---------------------------------------------------------------------------

def run_retraining():
    """
    Execute the full automated retraining pipeline:
      1. Run drift detection
      2. Log + skip if no drift (still writes a 'no_drift_skipped' MLflow run)
      3. Rebuild features on current data
      4. Load champion benchmark
      5. Train Random Forest challenger on same features
      6. Compare by AUC-ROC
      7. Promote or reject — always logged to MLflow
    """
    print("\n" + "="*55)
    print("RETRAINING PIPELINE — Starting")
    print("="*55)

    # Step 1 — Drift detection
    print("\n[Step 1] Running drift detection...")
    try:
        drift_report = run_drift_detection()
    except Exception as e:
        print(f"  [ERROR] Drift detection failed: {e}")
        raise

    # Handle both dict return and fallback to reading file
    if isinstance(drift_report, dict):
        verdict = drift_report.get("verdict", "unknown")
    else:
        saved = load_drift_report()
        verdict = saved["verdict"] if saved else "unknown"

    print(f"  Drift verdict: {verdict}")

    # Step 2 — Skip if no drift (but still log to MLflow)
    if verdict == "no_drift":
        print("\n[Step 2] No drift detected — skipping retraining.")
        run_id = log_skipped(verdict)
        print(f"  MLflow run logged: no_drift_skipped (run_id={run_id})")
        print("\n" + "="*55)
        return {
            "action":        "skipped",
            "reason":        "No significant drift detected.",
            "verdict":       verdict,
            "mlflow_run_id": run_id,
        }

    # Step 3 — Rebuild features
    print("\n[Step 3] Drift detected — rebuilding features...")
    try:
        run_feature_engineering()
        print("  Feature engineering complete.")
    except Exception as e:
        print(f"  [ERROR] Feature engineering failed: {e}")
        raise

    # Step 4 — Champion benchmark
    print("\n[Step 4] Loading champion metrics...")
    champion_metrics = load_champion_metrics()
    if champion_metrics is None:
        champion_metrics = {"auc_roc": 0.0, "f1_score": 0.0,
                            "precision": 0.0, "recall": 0.0}

    champion_feature_names = load_champion_feature_names()

    # Step 5 — Train challenger
    print("\n[Step 5] Training challenger model...")
    challenger_model, challenger_metrics, feature_columns = train_challenger(
        champion_feature_names=champion_feature_names
    )

    # Step 6 — Compare
    print("\n[Step 6] Comparing champion vs challenger...")
    comparison = compare_models(champion_metrics, challenger_metrics)
    print(f"  Champion AUC:   {comparison['champion_auc']}")
    print(f"  Challenger AUC: {comparison['challenger_auc']}")
    print(f"  Improvement:    {comparison['improvement']:+.4f}")
    print(f"  Decision:       {'PROMOTE' if comparison['promote_challenger'] else 'REJECT'}")

    # Step 7 — Log to MLflow
    print("\n[Step 7] Logging to MLflow...")
    run_id = log_and_promote(
        challenger_model, challenger_metrics, comparison, feature_columns, verdict
    )
    print(f"  MLflow run logged (run_id={run_id})")

    action = "promoted" if comparison["promote_challenger"] else "rejected"
    print(f"\n{'='*55}")
    print(f"RETRAINING PIPELINE — Complete: {action.upper()}")
    print("="*55 + "\n")

    return {
        "action":          action,
        "verdict":         verdict,
        "champion_auc":    comparison["champion_auc"],
        "challenger_auc":  comparison["challenger_auc"],
        "improvement":     comparison["improvement"],
        "mlflow_run_id":   run_id,
    }


if __name__ == "__main__":
    result = run_retraining()
    print("\nResult:", result)