"""
retrain.py - Phase 6: Automated Retraining

Reads the drift report from Phase 5 and decides whether to retrain.
If drift is significant, it:
    1. Re-runs feature engineering on current data
    2. Trains a new challenger model
    3. Compares challenger metrics against the current champion
    4. Promotes the challenger only if it outperforms the champion

This is the champion-challenger pattern — the production model is never
replaced unless the new one is proven better on the same test set.

Usage:
    python -m src.retraining.retrain
"""

import json
import mlflow
import mlflow.xgboost
import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.metrics import roc_auc_score, precision_score, recall_score, f1_score
from xgboost import XGBClassifier
from src.utils.config import Config
from src.utils.db import engine
from src.feature_engineering.build_features import run_feature_engineering
from src.drift_detection.detect_drift import run_drift_detection


# ---------------------------------------------------------------------------
# Drift report reader
# ---------------------------------------------------------------------------

def load_drift_report():
    """
    Read the drift report generated by Phase 5.
    Returns None if no report exists (drift detection hasn't run yet).
    """
    report_path = Config.INTERIM_DATA_DIR / "drift_report.json"

    if not report_path.exists():
        return None

    with open(report_path, "r") as f:
        return json.load(f)


# ---------------------------------------------------------------------------
# Champion model loader
# ---------------------------------------------------------------------------

def load_champion_metrics():
    """
    Retrieve the current production model's metrics from MLflow.

    Searches for the latest run in the churn_prediction experiment
    and returns its logged metrics. These serve as the benchmark
    the challenger must beat.
    """
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)

    client = mlflow.tracking.MlflowClient()
    experiment = client.get_experiment_by_name("churn_prediction")

    if experiment is None:
        return None

    # Get the most recent run
    runs = client.search_runs(
        experiment_ids=[experiment.experiment_id],
        order_by=["start_time DESC"],
        max_results=1,
    )

    if not runs:
        return None

    champion_run = runs[0]
    return {
        "run_id": champion_run.info.run_id,
        "auc_roc": champion_run.data.metrics.get("auc_roc", 0),
        "f1_score": champion_run.data.metrics.get("f1_score", 0),
        "precision": champion_run.data.metrics.get("precision", 0),
        "recall": champion_run.data.metrics.get("recall", 0),
    }


# ---------------------------------------------------------------------------
# Challenger model training
# ---------------------------------------------------------------------------

def train_challenger():
    """
    Train a new model on the current feature table.

    Uses the same hyperparameters from params.yaml but trains on
    the latest data (which may have been rebuilt by feature engineering).

    Returns:
        model: the trained XGBClassifier
        metrics: dict of evaluation metrics on the test set
        X_test, y_test: test data for further analysis if needed
    """
    params = Config.load_params()
    model_params = params["model"]
    training_params = params["training"]

    # Load current features
    with engine.connect() as conn:
        df = pd.read_sql("SELECT * FROM churn_features", conn)

    X = df.drop(columns=["account_id", "churn_flag"])
    y = df["churn_flag"]

    X_train, X_test, y_train, y_test = train_test_split(
        X, y,
        test_size=training_params["test_size"],
        random_state=model_params["random_state"],
        stratify=y,
    )

    # Handle class imbalance
    neg_count = (y_train == 0).sum()
    pos_count = (y_train == 1).sum()
    scale_pos_weight = neg_count / pos_count

    model = XGBClassifier(
        max_depth=model_params["max_depth"],
        learning_rate=model_params["learning_rate"],
        n_estimators=model_params["n_estimators"],
        random_state=model_params["random_state"],
        scale_pos_weight=scale_pos_weight,
        eval_metric="logloss",
    )

    model.fit(X_train, y_train)

    # Evaluate
    y_pred = model.predict(X_test)
    y_pred_proba = model.predict_proba(X_test)[:, 1]

    metrics = {
        "auc_roc": round(roc_auc_score(y_test, y_pred_proba), 4),
        "precision": round(precision_score(y_test, y_pred), 4),
        "recall": round(recall_score(y_test, y_pred), 4),
        "f1_score": round(f1_score(y_test, y_pred), 4),
    }

    return model, metrics, X_test, y_test


# ---------------------------------------------------------------------------
# Champion vs Challenger comparison
# ---------------------------------------------------------------------------

def compare_models(champion_metrics, challenger_metrics):
    """
    Decide whether the challenger should replace the champion.

    Primary metric: AUC-ROC (best overall measure of ranking quality).
    The challenger must beat the champion's AUC-ROC to be promoted.

    Returns:
        dict with comparison details and the promotion decision
    """
    champion_auc = champion_metrics.get("auc_roc", 0)
    challenger_auc = challenger_metrics.get("auc_roc", 0)

    promote = challenger_auc > champion_auc

    return {
        "champion_auc": champion_auc,
        "challenger_auc": challenger_auc,
        "improvement": round(challenger_auc - champion_auc, 4),
        "promote_challenger": promote,
    }


# ---------------------------------------------------------------------------
# MLflow logging and promotion
# ---------------------------------------------------------------------------

def log_and_promote(model, metrics, comparison, feature_columns):
    """
    Log the challenger model to MLflow. If it won the comparison,
    register it as the new production model version.
    """
    mlflow.set_tracking_uri(Config.MLFLOW_TRACKING_URI)
    mlflow.set_experiment("churn_prediction")

    run_name = "retrain_challenger" if comparison["promote_challenger"] else "retrain_rejected"

    with mlflow.start_run(run_name=run_name):
        # Log parameters
        params = Config.load_params()
        mlflow.log_param("model_type", "xgboost")
        mlflow.log_param("max_depth", params["model"]["max_depth"])
        mlflow.log_param("learning_rate", params["model"]["learning_rate"])
        mlflow.log_param("n_estimators", params["model"]["n_estimators"])
        mlflow.log_param("trigger", "drift_retraining")
        mlflow.log_param("num_features", len(feature_columns))

        # Log metrics
        for metric_name, metric_value in metrics.items():
            mlflow.log_metric(metric_name, metric_value)

        # Log comparison results
        mlflow.log_metric("champion_auc", comparison["champion_auc"])
        mlflow.log_metric("improvement", comparison["improvement"])
        mlflow.log_param("promoted", comparison["promote_challenger"])

        # Log the model — only register if promoted
        if comparison["promote_challenger"]:
            mlflow.xgboost.log_model(
                model,
                name="model",
                registered_model_name="churn_xgboost",
            )
        else:
            mlflow.xgboost.log_model(model, name="model")

        run_id = mlflow.active_run().info.run_id

    return run_id


# ---------------------------------------------------------------------------
# Pipeline orchestration
# ---------------------------------------------------------------------------

def run_retraining():
    """
    Execute the full automated retraining pipeline:
      1. Run drift detection
      2. Check if retraining is needed
      3. Rebuild features (in case data changed)
      4. Train challenger model
      5. Compare against champion
      6. Promote if better, reject if not
    """
    # Step 1: Run drift detection
    drift_report = run_drift_detection()
    verdict = drift_report["verdict"]

    # Step 2: Check if retraining is warranted
    if verdict == "no_drift":
        return {
            "action": "skipped",
            "reason": "No significant drift detected",
            "verdict": verdict,
        }

    # Step 3: Rebuild features on current data
    run_feature_engineering()

    # Step 4: Get champion metrics
    champion_metrics = load_champion_metrics()
    if champion_metrics is None:
        champion_metrics = {"auc_roc": 0, "f1_score": 0, "precision": 0, "recall": 0}

    # Step 5: Train challenger
    challenger_model, challenger_metrics, X_test, y_test = train_challenger()

    # Step 6: Compare
    comparison = compare_models(champion_metrics, challenger_metrics)

    # Step 7: Log and optionally promote
    feature_columns = X_test.columns.tolist()
    run_id = log_and_promote(
        challenger_model, challenger_metrics, comparison, feature_columns
    )

    return {
        "action": "promoted" if comparison["promote_challenger"] else "rejected",
        "verdict": verdict,
        "champion_auc": comparison["champion_auc"],
        "challenger_auc": comparison["challenger_auc"],
        "improvement": comparison["improvement"],
        "mlflow_run_id": run_id,
    }


if __name__ == "__main__":
    run_retraining()